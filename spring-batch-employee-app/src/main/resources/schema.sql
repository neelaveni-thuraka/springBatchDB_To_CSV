DROP TABLE IF EXISTS TABLE_EMPLOYEES_DATA_APP;
  
CREATE TABLE TABLE_EMPLOYEES_DATA_APP (
  EMP_ID INT NOT NULL,
  EMP_FIRSTNAME VARCHAR(250) NOT NULL,
  EMP_MIDDLENAME VARCHAR(250) NOT NULL,
  EMP_LASTNAME VARCHAR(250) NOT NULL,
  EMP_EMAILID VARCHAR(250) DEFAULT NULL,
  EMP_ORGANIZATION VARCHAR(250) NOT NULL,
  EMP_COUNTRY VARCHAR(250) NOT NULL,
  EMP_STATE VARCHAR(250) NOT NULL,
  EMP_ADDRESS VARCHAR(250) NOT NULL
);

